   public void search(){
        try{
            conn=MySQLConnect.ConnectDb();
           //Databaase Row  Calculating
            PreparedStatement ps1 = conn.prepareStatement("Select * from student where sname=?");
            ps1.setString(1, jTextField1.getText());
            ResultSet rs1 = ps1.executeQuery();
            int i=0;            
            while(rs1.next()){
             ++i;
            }
            if(i==0){
               JOptionPane.showMessageDialog(null, "No Records Found"); 
            }else if(i>0){
                jTextField2.setText(Integer.toString(i));
            }
            
            //Databaase Row Calculating - Present
            PreparedStatement ps2 = conn.prepareStatement("Select * from student where sname=? and attendance='Present' ");
            ps2.setString(1, jTextField1.getText());
            ResultSet rs2 = ps2.executeQuery();
            int j=0;            
            while(rs2.next()){
             ++j;
            }
            if(j==0){
              // JOptionPane.showMessageDialog(null, "No Days Present"); 
              jTextField3.setText(Integer.toString(j));
            }else if(j>0){
                jTextField3.setText(Integer.toString(j));
            }
            
            
            //Databaase Row Calculating - Absent
            PreparedStatement ps3 = conn.prepareStatement("Select * from student where sname=? and attendance = 'Absent' ");
            ps3.setString(1, jTextField1.getText());
            ResultSet rs3 = ps3.executeQuery();
            int k=0;            
            while(rs3.next()){
             ++k;
            }
            if(k==0){
              // JOptionPane.showMessageDialog(null, "No Days Absent"); 
              jTextField4.setText(Integer.toString(k));
            }else if(k>0){
                jTextField4.setText(Integer.toString(k));
            }

            double attend_percent =0;
            attend_percent=((j*100)/i);
            jTextField6.setText(Double.toString(attend_percent)+" %");
             
            conn.close();
        }
        catch(Exception e){
           JOptionPane.showMessageDialog(rootPane, e);
        }
   } 
    
   
